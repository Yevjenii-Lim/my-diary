// Test script to verify OpenAI API integration
require('dotenv').config({ path: '.env.local' });

async function testOpenAIIntegration() {
  console.log('ü§ñ Testing OpenAI API Integration...\n');

  // Check if API key is set
  const apiKey = process.env.OPENAI_API_KEY;
  if (!apiKey) {
    console.log('‚ùå OPENAI_API_KEY not found in .env.local');
    console.log('Please add your OpenAI API key to .env.local');
    return;
  }

  console.log('‚úÖ OpenAI API key found');

  // Test API call
  try {
    console.log('üîÑ Testing API connection...');
    
    const response = await fetch('https://api.openai.com/v1/chat/completions', {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${apiKey}`,
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        model: 'gpt-3.5-turbo',
        messages: [
          {
            role: 'system',
            content: 'You are a helpful assistant. Respond with "AI integration test successful!"'
          },
          {
            role: 'user',
            content: 'Test message'
          }
        ],
        max_tokens: 50
      })
    });

    if (!response.ok) {
      throw new Error(`API Error: ${response.status} ${response.statusText}`);
    }

    const data = await response.json();
    const message = data.choices[0]?.message?.content;
    
    console.log('‚úÖ OpenAI API connection successful!');
    console.log(`üìù Response: ${message}`);
    console.log('\nüéâ AI integration is ready to use!');
    console.log('\nYou can now:');
    console.log('1. Visit any topic page in your diary app');
    console.log('2. Click on the "AI Suggestions" tab');
    console.log('3. See personalized writing suggestions generated by AI');

  } catch (error) {
    console.log('‚ùå OpenAI API test failed:');
    console.log(error.message);
    
    if (error.message.includes('401')) {
      console.log('\nüí° This usually means your API key is invalid or expired.');
      console.log('Please check your OpenAI API key in .env.local');
    } else if (error.message.includes('429')) {
      console.log('\nüí° Rate limit exceeded. Please wait a moment and try again.');
    } else {
      console.log('\nüí° Check your internet connection and try again.');
    }
  }
}

// Run the test
testOpenAIIntegration();
